package ee.adit.pojo;

public class SuccessMonitor {

	boolean success;

	public SuccessMonitor() {
		;
	}
	
	public SuccessMonitor(boolean success) {
		this.success = success;
	}
	
	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}
	
}
